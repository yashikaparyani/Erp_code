const METHOD_ALIASES: Record<string, string> = {
  waive_sla_penal: 'waive_sla_penalty',
};

export async function POST(request: NextRequest) {
  try {
    const payload = await request.json();
    const requestedMethod = String(payload?.method || '').trim();
    const method = METHOD_ALIASES[requestedMethod] || requestedMethod;
    const args = payload?.args && typeof payload.args === 'object' ? payload.args : {};

    if (!method) {
      return NextResponse.json({ success: false, message: 'method is required' }, { status: 400 });
    }

    if (!CONNECTED_METHODS.has(method)) {
      return NextResponse.json(
        { success: false, message: `Method not allowlisted for frontend connector: ${method}` },
        { status: 400 },
      );
    }

    const result = await callFrappeMethod(method, args, request);
    return NextResponse.json({ success: true, data: result?.data ?? result, message: result?.message });
  } catch (error) {
    return jsonErrorResponse(error, 'Failed to execute operation');
  }
}
